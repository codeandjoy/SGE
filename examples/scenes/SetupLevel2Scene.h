sge::Scene* setupLevel2Scene(sge::Universe* universe, sf::View* levelView){
    sge::Scene* level2 = new sge::Scene();



    std::vector<sge::Entity*> mapTileEntities;

    for(int i = 0; i < level2::tileCountY; i++){
        for(int j = 0; j < level2::tileCountX; j++){
            if(level2::map_data[level2::tileCountX*i+j] != 0){
                mapTileEntities.push_back(new sge::StaticEntity(
                    universe->assetsManager->getTextureSheet("tileset")->getTexture(),
                    universe->assetsManager->getTextureSheet("tileset")->getTextureRect(level2::map_data[level2::tileCountX*i+j] - 1),
                    sf::Vector2f(j*level2::tileSizeX, i*level2::tileSizeY),
                    {"tiles"}
                ));
            }
        }
    }



    sge::Entity* coinEntity = new sge::StaticEntity(
        universe->assetsManager->getTextureSheet("tileset")->getTexture(),
        universe->assetsManager->getTextureSheet("tileset")->getTextureRect(42),
        sf::Vector2f(400, 240),
        {"heart"}
    );



    level2->addEntities(levelView, mapTileEntities);
    level2->addEntity(levelView, coinEntity);



    return level2;
};