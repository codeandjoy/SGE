sge::Scene* setupLevel1Scene(sge::Universe* universe, sf::View* levelView){
    sge::Scene* level1 = new sge::Scene();



    std::vector<sge::Entity*> mapTileEntities;

    for(int i = 0; i < level1::tileCountY; i++){
        for(int j = 0; j < level1::tileCountX; j++){
            if(level1::map_data[level1::tileCountX*i+j] != 0){
                mapTileEntities.push_back(new sge::StaticEntity(
                    universe->assetsManager->getTextureSheet("tileset")->getTexture(),
                    universe->assetsManager->getTextureSheet("tileset")->getTextureRect(level1::map_data[level1::tileCountX*i+j] - 1),
                    sf::Vector2f(j*level1::tileSizeX, i*level1::tileSizeY),
                    {"tiles"}
                ));
            }
        }
    }



    sge::Entity* coinEntity = new sge::StaticEntity(
        universe->assetsManager->getTextureSheet("tileset")->getTexture(),
        universe->assetsManager->getTextureSheet("tileset")->getTextureRect(2),
        sf::Vector2f(400, 240),
        {"coin"}
    );



    level1->addEntities(levelView, mapTileEntities);
    level1->addEntity(levelView, coinEntity);



    return level1;
};